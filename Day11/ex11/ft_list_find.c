#include "ft_list.h"

t_list	*ft_list_find(t_list *begin_list, void *data_ref, int (*cmp)())
{
	t_list	*start;

	start = begin_list;
	while (start && cmp(start->data, data_ref))
		start = start->next;
	return (start);
}
