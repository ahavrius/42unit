int	ft_is_prime(int nb)
{
	int i;
	int c;

	if (nb <= 1)
	{
		return (0);
	}
	i = 2;
	c = 1;
	while (i < nb / 2 && c == 1)
	{
		if (nb % i == 0)
		{
			c = 0;
		}
		i++;
	}
	return (c);
}
